package fr.utbm.info.ia51.labwork1.environment.agent;

import fr.utbm.info.ia51.labwork1.environment.maze.Direction;
import io.sarl.lang.annotation.SarlElementType;
import io.sarl.lang.annotation.SarlSpecification;
import io.sarl.lang.annotation.SyntheticMember;
import java.util.Objects;
import java.util.UUID;
import org.eclipse.xtext.xbase.lib.Pure;

/**
 * Describes a query for the maze change.
 * 
 * @author St&eacute;phane GALLAND &lt;stephane.galland@utbm.fr&gt;
 * @version $Name$ $Revision$ $Date$
 */
@SarlSpecification("0.5")
@SarlElementType(8)
@SuppressWarnings("all")
final class MazeChangeQuery {
  private final UUID emitter;
  
  private final Direction direction;
  
  public MazeChangeQuery(final UUID emitter, final Direction direction) {
    this.emitter = emitter;
    this.direction = direction;
  }
  
  /**
   * Replies the emitter.
   */
  @Pure
  public UUID getEmitter() {
    return this.emitter;
  }
  
  /**
   * Replies the change.
   */
  @Pure
  public Direction getChange() {
    return this.direction;
  }
  
  @Override
  @Pure
  @SyntheticMember
  public boolean equals(final Object obj) {
    if (this == obj)
      return true;
    if (obj == null)
      return false;
    if (getClass() != obj.getClass())
      return false;
    MazeChangeQuery other = (MazeChangeQuery) obj;
    if (!Objects.equals(this.emitter, other.emitter)) {
      return false;
    }
    return super.equals(obj);
  }
  
  @Override
  @Pure
  @SyntheticMember
  public int hashCode() {
    int result = super.hashCode();
    final int prime = 31;
    result = prime * result + Objects.hashCode(this.emitter);
    return result;
  }
}
